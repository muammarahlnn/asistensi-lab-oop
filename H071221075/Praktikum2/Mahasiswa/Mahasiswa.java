package TugasOOP2.Mahasiswa;

public class Mahasiswa {
    String nama;
    String nim;
    Alamat alamat;

    public String getNama() {

        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getNim() {

        return nim;
    }

    public void setNim(String nim) {

        this.nim = nim;
    }
    public Alamat getAlamat() {
         return this.alamat;
    }
}
